    import re

    @staticmethod
    def detect_category(gujarati_text: str):
        """
        Returns (category_name, matched_word)
        Example тЖТ ("ркХрк┐рк╕рк╛рки/ркХрлГрк╖рк┐ ркмрк╛ркмркд", "ркЦрлЗркбрлВркд")
        """
        text = gujarati_text.strip()

        for category, keywords in GovtInfo.GOVT_CAT_KEYWORDS.items():
            for word in keywords:
                # Whole-word match using regex
                pattern = rf"\b{re.escape(word)}\b"
                if re.search(pattern, text):
                    cat_id = GovtInfo.govt_cat_id_mapping.get(category)
                    return category, word, cat_id

        return "Unknown", None, 0


    @staticmethod
    def detect_district(gujarati_text: str):
        """
        Detects the district and taluka name from Gujarati text.
        Returns (district_name, matched_taluka, district_id, taluka_id)
        Example тЖТ ("ркХркЪрлНркЫ", "ркнрлВркЬ", 1, "01005")
        """
        for district, talukas in GovtInfo.DISTRICT_MAP.items():
            for taluka in talukas:
                if taluka in gujarati_text:
                    # find district_id from name
                    dis_id = next((k for k, v in GovtInfo.district_id_mapping.items() if v == district), None)
                    # find taluka_id from name
                    tal_id = next((k for k, v in GovtInfo.taluka_id_mapping.items() if v == taluka), None)
                    return district, taluka, dis_id, tal_id
        return "Unknown", None, None, None

    @staticmethod
    def detect_district_old(gujarati_text: str):
        """
        Detects the district and matching taluka/zone name from the Gujarati text.
        Returns (district_name, matched_word)
        Example тЖТ ("ркЕркоркжрк╛рк╡рк╛ркж", "рк╡рк┐рк░ркоркЧрк╛рко")
        """
        for district, areas in GovtInfo.DISTRICT_MAP.items():
            for area in areas:
                if area in gujarati_text:
                    dis_id = GovtInfo.district_id_mapping.get(district)
                    tal_id = GovtInfo.taluka_id_mapping.get(area)
                    return district, area, dis_id, tal_id
        return "Unknown", None, None, None


    @staticmethod
    def detect_district_whole_word(gujarati_text: str):
        """
        Detects district and taluka by whole word match, starting from the beginning of the input string.
        Returns (district_name, taluka_name, district_id, taluka_id)
        """
        words = gujarati_text.strip().split()
        if not words:
            return "Unknown", None, None, None

        # Only consider matches at the start of the string
        first_word = words[0]

        for district, talukas in GovtInfo.DISTRICT_MAP.items():
            if district == first_word:
                dis_id = next((k for k, v in GovtInfo.district_id_mapping.items() if v == district), None)
                # Check if the next word matches a taluka
                if len(words) > 1:
                    second_word = words[1]
                    for taluka in talukas:
                        if taluka == second_word:
                            tal_id = next((k for k, v in GovtInfo.taluka_id_mapping.items() if v == taluka), None)
                            return district, taluka, dis_id, tal_id
                # If only district matches, return district
                return district, None, dis_id, None

            # If first word matches a taluka in any district
            for taluka in talukas:
                if taluka == first_word:
                    dis_id = next((k for k, v in GovtInfo.district_id_mapping.items() if v == district), None)
                    tal_id = next((k for k, v in GovtInfo.taluka_id_mapping.items() if v == taluka), None)
                    return district, taluka, dis_id, tal_id

        return "Unknown", None, None, None

